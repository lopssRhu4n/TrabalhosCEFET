
public class Teclado {
  private int numeroDeSons;
  private double preco;

  public void setNumeroDeSons(int num) {
    this.numeroDeSons = num;
  }

  public void setPreco(double preco) {
    this.preco = preco;
  }

  public int getNumeroDeSons() {
    return numeroDeSons;
  }

  public double getPreco() {
    return preco;
  }
}
